function compararPersonagens() {
  let vitoriaForca = "";
  let vitoriaVelocidade = "";
  let vitoriaResistencia = "";
  let vitoriaOverall = "";

  for (let i = 0; i < personagens.length; i++) {
    for (let j = i + 1; j < personagens.length; j++) {
      const p1 = personagens[i];
      const p2 = personagens[j];

      const p1ForcaTotal =
        p1.forca +
        (p1.armaPrincipal === "Escudo americano"
          ? 10
          : p1.armaPrincipal === "Armadura de Ferro"
          ? 20
          : 0);
      const p2ForcaTotal =
        p2.forca +
        (p2.armaPrincipal === "Escudo americano"
          ? 10
          : p2.armaPrincipal === "Armadura de Ferro"
          ? 20
          : 0);

      if (p1ForcaTotal > p2ForcaTotal) {
        vitoriaForca = `${p1.codinome} vence em força contra ${p2.codinome}.`;
      } else if (p1ForcaTotal < p2ForcaTotal) {
        vitoriaForca = `${p2.codinome} vence em força contra ${p1.codinome}.`;
      } else {
        vitoriaForca = `${p1.codinome} e ${p2.codinome} têm a mesma força.`;
      }

      if (p1.velocidade > p2.velocidade) {
        vitoriaVelocidade = `${p1.codinome} vence em velocidade contra ${p2.codinome}.`;
      } else if (p1.velocidade < p2.velocidade) {
        vitoriaVelocidade = `${p2.codinome} vence em velocidade contra ${p1.codinome}.`;
      } else {
        vitoriaVelocidade = `${p1.codinome} e ${p2.codinome} têm a mesma velocidade.`;
      }

      if (p1.resistencia > p2.resistencia) {
        vitoriaResistencia = `${p1.codinome} vence em resistência contra ${p2.codinome}.`;
      } else if (p1.resistencia < p2.resistencia) {
        vitoriaResistencia = `${p2.codinome} vence em resistência contra ${p1.codinome}.`;
      } else {
        vitoriaResistencia = `${p1.codinome} e ${p2.codinome} têm a mesma resistência.`;
      }

      const p1Overall = p1ForcaTotal + p1.velocidade + p1.resistencia;
      const p2Overall = p2ForcaTotal + p2.velocidade + p2.resistencia;

      if (p1Overall > p2Overall) {
        vitoriaOverall = `${p1.codinome} vence no geral contra ${p2.codinome}.`;
      } else if (p1Overall < p2Overall) {
        vitoriaOverall = `${p2.codinome} vence no geral contra ${p1.codinome}.`;
      } else {
        vitoriaOverall = `${p1.codinome} e ${p2.codinome} têm o mesmo desempenho no geral.`;
      }

      console.log(p1.descricao());
      console.log(p2.descricao());
      console.log(vitoriaForca);
      console.log(vitoriaVelocidade);
      console.log(vitoriaResistencia);
      console.log(vitoriaOverall);
      console.log("---------------------------------");
    }
  }
}

compararPersonagens();